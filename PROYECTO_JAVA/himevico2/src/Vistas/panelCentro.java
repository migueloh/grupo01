             controladora.modificarC(tnombreC.getText(),tcalleC.getText(),Integer.parseInt(tnumeroC.getText()),Integer.parseInt( tcodpostalC.getText()),tciudadC.getText(),tprovinciaC.getText(),Integer.parseInt(ttlfC.getText()));
            
